import math

# Использование констант 
print (math. pi )   # Выведет приблизительное значение π

# Тригонометрия 
angle = math.radians( 60 )   # Конвертация из градусов в радианы 
print (math. sin (angle))   # Синус угла

# Корень числа 
print (math. sqrt ( 9 ))   # Квадратный корень из 9

# Логарифмы 
print (math. log ( 10 , 2 ))   # Логарифм 10 по основанию 2