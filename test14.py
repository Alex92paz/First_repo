first = 10
second = 5

# Шаг 1: Сумма чисел first и second, возведение в степень 3
sum_result = (first + second) ** 3

# Шаг 2: Разность чисел first и second, возведение в степень 2
diff_result = (first - second) ** 2

# Шаг 3: Сложение результатов шагов 1 и 2
total_result = sum_result + diff_result

# Шаг 4: Деление суммы на 3 без остатка
expression = total_result // 3

# Вывод результата
print(expression)
