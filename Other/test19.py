n = 5000

# Вычисление полных часов
hours = n // (60 * 60)
'''
60×60=3600 секунд в одном часе.
5000÷3600=1 полный час.
'''

# Вычисление полных минут из оставшихся секунд после учёта часов
minutes = (n - hours * 60 * 60) // 60
'''
Полные часы: 1×3600=3600 секунд.
Оставшиеся секунды: 5000−3600=1400 секунд.
1400÷60=23 полные минуты.
'''

# Вычисление оставшихся секунд после учёта часов и минут
seconds = n - hours * 60 * 60 - minutes * 60
'''
Полные часы: 1×3600=3600 секунд.
Полные минуты: 23×60=1380 секунд.
Оставшиеся секунды: 5000−3600−1380=20 секунд.
'''

# Правильное форматирование строки для вывода
print(f"{hours} hours, {minutes} minutes, {seconds} seconds")
