some_data = 10
msg = some_data or "Не було повернено даних"
print(msg)


'''
Без використання скороченої форми тернарного
оператора з or, вираз msg = some_data or "Не
було повернено даних" може бути переписаний з 
використанням стандартного умовного оператора
if ... else ... наступним чином:
'''
some_data = None
if some_data:
    msg = some_data
else:
    msg = "Не було повернено даних"

print(msg)