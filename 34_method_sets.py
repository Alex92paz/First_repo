'''
Множини — це неврегульований контейнер, який містить тільки унікальні елементи.
У множину можна додавати тільки незмінні типи даних.
Щоб створити порожню множину, потрібно викликати функцію set()
'''
#Нехай у нас є список, з якого треба видалити дублікати
lst = [1, 2, 3, 1, 2, 2, 3, 4, 1]
#Це можна зробити, перетворивши список у множину
d_lst = set(lst)
#А далі множину перетворити знову у список
lst = list(d_lst)
print(lst)
