# Створення порожнього списку
my_list = []

# Додавання елементів до списку за вказаними індексами
my_list.insert(0, 2024)      # На позицію з індексом 0 помістіть ціле число 2024
my_list.insert(1, 'Python')  # На позицію з індексом 1 помістіть рядок 'Python'
my_list.insert(2, 3.12)      # На позицію з індексом 2 помістіть дійсне число 3.12

# Друк результату
print(my_list)
